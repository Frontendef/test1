const closePopUp16 = document.getElementById('pop_up_close16');
const popUp16 = document.getElementById('pop_up16');



openPopUp16.addEventListener('click', function(e){
    e.preventDefault();
    popUp16.classList.add('active');
})

closePopUp16.addEventListener('click', () => {
    popUp16.classList.remove('active');
})


